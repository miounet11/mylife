// ── 槽位填充工具 ──

export function fillTemplate(template: PhraseTemplate, values: Record<string, string>): string {
  let result = template.template;
  for (const slot of template.slots) {
    const val = values[slot] || `[${slot}]`;
    result = result.replace(`{${slot}}`, val);
  }
  return result;
}

export function pickTemplate(templates: PhraseTemplate[], seed: number, style?: 'premium' | 'daily'): PhraseTemplate {
  const filtered = style ? templates.filter(t => t.style === style) : templates;
  return filtered[seed % filtered.length];
}

export function pickFromList(list: string[], seed: number): string {
  return list[seed % list.length];
}
