/** UI / on-image label language for SEO & seed markets */
export type PageIllustLocale = 'zh-CN' | 'zh-Hant' | 'en';

export type PageIllustrationEntry = {
  id: string;
  surfaces: string[];
  role: PageIllustRole;
  title: string;
  caption: string;
  alt: string;
  filename: string;
  aspectRatio: '16:9' | '3:2' | '1:1';
  /** Prompt body without style suffix */
  prompt: string;
  /**
   * Generation routing for inping API:
   * - complex → gpt-image-2 (multi-panel, Chinese labels)
   * - simple  → z-image-turbo (icons / tiny assets, ~20× faster)
   */
  complexity?: 'complex' | 'simple';
  reportCiteKeys?: string[];
  tags: string[];
  /** Set true after asset exists in public/ */
  ready: boolean;
  width?: number;
  height?: number;
  /**
   * On-image label language. Default zh-CN (Simplified).
   * Multi-locale variants share surfaces; resolver prefers matching locale.
   */
  locale?: PageIllustLocale;
  /** Base id when this is a locale variant, e.g. PI-HOME-01 */
  variantOf?: string;
};

